package com.example.demo.model.reversar;

public class PagoR {
    private IdentificadorPagoR identificadorPago;
    private InfoPagoR infoPago;
    private EstadoPagoR estadoPago;

    public IdentificadorPagoR getIdentificadorPago() {
        return identificadorPago;
    }

    public void setIdentificadorPago(IdentificadorPagoR identificadorPago) {
        this.identificadorPago = identificadorPago;
    }

    public InfoPagoR getInfoPago() {
        return infoPago;
    }

    public void setInfoPago(InfoPagoR infoPago) {
        this.infoPago = infoPago;
    }

    public EstadoPagoR getEstadoPago() {
        return estadoPago;
    }

    public void setEstadoPago(EstadoPagoR estadoPago) {
        this.estadoPago = estadoPago;
    }
}
